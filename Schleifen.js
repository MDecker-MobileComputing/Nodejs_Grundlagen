#!/usr/bin/env node

console.log();

// While-Schleife
let i1 = 1;
while (i1 <= 5) {
    console.log("i1=" + i1);
    i1++;
}
console.log();


// Do-While-Schleife
let i2 = 1;
do {
    console.log("i2=" + i2);
    i2++;
} while (i2 <= 5);
console.log();


// For-Schleife
for (let i3 = 1; i3 <= 5; i3++) {
    console.log("i3=" + i3);
}
console.log();